let currentLang = localStorage.getItem('quiz_lang') || 'uz';

function t(key) {
    return translations[currentLang][key] || key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[currentLang] && translations[currentLang][key]) {
            el.setAttribute('placeholder', translations[currentLang][key]);
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();

    const langBtns = document.querySelectorAll('.lang-btn');
    langBtns.forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }

        btn.addEventListener('click', (e) => {
            langBtns.forEach(b => b.classList.remove('active'));
            e.target.classList.add('active');
            currentLang = e.target.getAttribute('data-lang');
            localStorage.setItem('quiz_lang', currentLang);
            applyTranslations();
            
            // Re-render components if methods are available in global scope
            if (typeof renderLeaderboard === 'function' && !document.getElementById('auth-screen').classList.contains('hidden')) {
                renderLeaderboard();
            }
            if (typeof renderQuestionsList === 'function' && !document.getElementById('admin-panel').classList.contains('hidden')) {
                renderQuestionsList();
                if (typeof renderResultsTable === 'function') {
                    renderResultsTable();
                }
            }
        });
    });
});


